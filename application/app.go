package application

import (
	"context"
	"fmt"
	"net/http"

	"github.com/redis/go-redis/v9"
)

type App struct {
	router http.Handler
	rdb    *redis.Client
}

func New() *App {
	return &App{
		router: loadRoutes(),
		rdb:    redis.NewClient(&redis.Options{}),
	}
}

func (a *App) Start(ctx context.Context) error {
	server := &http.Server{
		Addr:    ":3000",
		Handler: a.router,
	}

	err := a.rdb.Ping(ctx).Err()
	if err != nil {
		fmt.Errorf("Failed to connect to redis %w", err)
	}

	fmt.Println("Starting server")

	err = server.ListenAndServe()
	if err != nil {
		fmt.Errorf("Failed to start server: %w", err)
	}

	return nil
}
